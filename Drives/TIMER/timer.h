void TIM4_CH4_PWM_Init(u16 arr,u16 psc);								//红外发射初始化
void SendData1(u8 SysNum, u8 KeyNum);										//红外发射
void TIM4_CH3_PWM_Init(u16 arr,u16 psc);								//红外发射初始化
void SendData2(u8 SysNum, u8 KeyNum);										//红外发射
void TIM1_CH1N_PWM_Init(u16 arr,u16 psc);								//power_led初始化
void PWM_SetDuty3(uint8_t duty);												//灯光变化
#endif

